package graph.math;

public class Line {

	private double k;
	private double n;
	
	public Line(double k, double n) {
		super();
		this.k = k;
		this.n = n;
	}
	
	public double getK() {
		return k;
	}
	public void setK(double k) {
		this.k = k;
	}
	public double getN() {
		return n;
	}
	public void setN(double n) {
		this.n = n;
	}
}
